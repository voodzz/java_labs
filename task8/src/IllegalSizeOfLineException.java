public class IllegalSizeOfLineException extends IllegalArgumentException {
    public IllegalSizeOfLineException(String message) {
        super(message);
    }

    public IllegalSizeOfLineException(String message, Throwable cause) {
        super(message, cause);
    }

    public IllegalSizeOfLineException(Throwable cause) {
        super(cause);
    }

    public IllegalSizeOfLineException() {
    }
}
